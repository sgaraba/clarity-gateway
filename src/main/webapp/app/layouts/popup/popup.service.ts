import { Injectable, Type } from '@angular/core';
import { Subject } from 'rxjs/Subject';
import { PopupItem } from 'app/layouts/popup/popup-item.model';

@Injectable()
export class PopupService {
    private _component: Subject<any>;
    constructor() {
        this._component = new Subject<any>();
    }

    get component(): Subject<any> {
        return this._component;
    }

    load(component: Type<any>, params: any): void {
        this._component.next(new PopupItem(component, params));
    }

    eject() {
        this._component.next(null);
    }
}
